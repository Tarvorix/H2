export function normalizeAlphaPromotionGateResult(gateSummary, expectedModelId, force = false) {
  if (!gateSummary) {
    if (force) return null;
    throw new Error('Promotion requires --gate-summary unless --force is provided.');
  }

  if (gateSummary.candidateModelId !== expectedModelId) {
    throw new Error(
      `Gate summary candidate "${gateSummary.candidateModelId}" does not match model "${expectedModelId}".`,
    );
  }

  const threshold = Number(gateSummary.threshold ?? 0);
  const winRate = Number(gateSummary.winRate ?? 0);
  const passed = typeof gateSummary.passed === 'boolean'
    ? gateSummary.passed
    : winRate > threshold;

  if (!passed && !force) {
    throw new Error('Gate summary did not pass. Re-run with --force if you intentionally want to bypass gate failure.');
  }

  return {
    passed,
    winRate,
    candidateWins: Number(gateSummary.candidateWins ?? 0),
    engineWins: Number(gateSummary.engineWins ?? 0),
    tacticalWins: Number(gateSummary.tacticalWins ?? 0),
    defaultAlphaWins: Number(gateSummary.defaultAlphaWins ?? 0),
    draws: Number(gateSummary.draws ?? 0),
    aborted: Number(gateSummary.aborted ?? 0),
    timeouts: Number(gateSummary.timeouts ?? 0),
    threshold,
    timeBudgetMs: Number(gateSummary.timeBudgetMs ?? 0),
    maxSimulations: Number(gateSummary.maxSimulations ?? 0),
    matchesPerOpponent: Number(gateSummary.matchesPerOpponent ?? 0),
  };
}

