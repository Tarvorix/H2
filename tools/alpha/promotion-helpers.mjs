function sanitizeArchiveSegment(value) {
  return String(value)
    .trim()
    .replace(/[^a-zA-Z0-9._-]+/g, '-')
    .replace(/^-+|-+$/g, '')
    || 'promotion';
}

export function createAlphaPromotionArchiveDirName(sourceModelId, promotedAt) {
  const normalizedTimestamp = promotedAt.replace(/[:.]/g, '-');
  return `${normalizedTimestamp}-${sanitizeArchiveSegment(sourceModelId)}`;
}

